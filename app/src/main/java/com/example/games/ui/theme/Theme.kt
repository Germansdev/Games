@Composable
fun GamesTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors =

        if (darkTheme) {
        DarkColors

    } else {
        LightColors

    }




    val systemUiController = rememberSystemUiController()

    SideEffect {
// with only this: both top and bottom with the same color
        systemUiController.setSystemBarsColor(
            if (darkTheme) Color.Black else Color.White
        )


//whith only this: bottom color:
        /**systemUiController.setNavigationBarColor(
            color = if (darkTheme) Color.Yellow else Color.White
        ) */

//whith only this: top color with different color:
        /**systemUiController.setStatusBarColor(
            color = if(darkTheme) Color.Yellow else Color.White
        )*/
    }


    // Background theme
/**
    val backgroundTheme =
        if (darkTheme) DarkAndroidBackgroundTheme else LightAndroidBackgroundTheme

*/


    androidx.compose.material3.MaterialTheme(
        colorScheme = colors,
        content = content,
        typography = androidx.compose.material3.MaterialTheme.typography
    )
}

